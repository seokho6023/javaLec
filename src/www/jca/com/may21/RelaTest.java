public class RelaTest {
	public static void main(String[] args) {
		char a,b;
		a = 'A';
		b = 'B';
		
		System.out.println((int)a);
		System.out.println((int)b);
		System.out.println("a>b->"+(a>b));
		System.out.println("a<b->"+(a<b));
		System.out.println("a==b->"+(a==b));
		System.out.println("a!=b->"+(a!=b));
	}
}
